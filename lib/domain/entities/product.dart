class Product {
  String id;
  String imageName;
  String productName;
  String productContent;
  String seller;
  String price;
  String address;
  int favorite;
  int chat;

  Product({
    required this.id,
    required this.imageName,
    required this.productName,
    required this.productContent,
    required this.seller,
    required this.price,
    required this.address,
    required this.favorite,
    required this.chat,
  });
}
